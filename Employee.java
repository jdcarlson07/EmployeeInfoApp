package com.example.employeeinfo;

public class Employee {

    String firstName;
    String lastName;
    int age;
    int height;
    int weight;

    public Employee(String fname, String lname, int a, int h, int w)
    {
        this.firstName = fname;
        this.lastName = lname;
        this.age= a;
        this.height = h;
        this.weight = w;

    }
}
